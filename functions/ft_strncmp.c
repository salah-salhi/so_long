#include "../so_long.h"

int	ft_strncmp(char *s1, char *s2, int n)
{
	int	i;

	i = 0;
	while (i < n && (s1 [i] || s2 [i]))
	{
		if (s1 [i] != s2 [i])
			return (s1 [i] - s2 [i]);
				i++;
	}
	return (0);
}
